package Models;

public class InitialClusterDetails {
	private int numberofplans;
	private double averagedistanceot;
	private double averagedistancehd;

	public int getNumberofplans() {
		return numberofplans;
	}
	public void setNumberofplans(int numberofplans) {
		this.numberofplans = numberofplans;
	}
	public double getAveragedistanceot() {
		return averagedistanceot;
	}
	public void setAveragedistanceot(double averagedistanceot) {
		this.averagedistanceot = averagedistanceot;
	}
	public double getAveragedistancehd() {
		return averagedistancehd;
	}
	public void setAveragedistancehd(double averagedistancehd) {
		this.averagedistancehd = averagedistancehd;
	}
}
