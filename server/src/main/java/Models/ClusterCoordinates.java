package Models;

public class ClusterCoordinates {
	private double[] x;
	private double[] y;
	private int[] radius;
	public double[] getX() {
		return x;
	}
	public void setX(double[] x) {
		this.x = x;
	}
	public double[] getY() {
		return y;
	}
	public void setY(double[] y) {
		this.y = y;
	}
	public int[] getRadius() {
		return radius;
	}
	public void setRadius(int[] radius) {
		this.radius = radius;
	}
	
	
}
